bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        set_keylog(keycode, record);
    }
    mod_state = get_mods();

    switch (keycode) {
        case KC_LCTL:
        case KC_RCTL:
#    ifdef OCEAN_DREAM_ENABLE
            is_calm = (record->event.pressed) ? true : false;
#    endif
#    ifdef LUNA_ENABLE
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
#    endif
            break;
        case KC_SPC:
#    ifdef LUNA_ENABLE
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
#    endif
            break;
    }
    return true;
}

